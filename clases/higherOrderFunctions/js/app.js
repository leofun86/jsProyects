// map --> Realiza determinada acción con cada elemento del array; puede almacenarlo en un array
	console.log('\nMAP');
	const arr2 = [];
	autos.map(auto => {
		if (auto.year === 2017) arr2.push(auto);
	});
	console.table(arr2);
